#include "libft.h"
#include <stdlib.h>

void	*ft_memalloc(size_t size)
{
	void	*new;

	if (size <= 0)
		return (NULL);
	if ((new = malloc(sizeof(void*) * size)))
		ft_bzero(new, size);
	else
		new = NULL;
	return (new);
}
