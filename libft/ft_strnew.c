#include <stdlib.h>
#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*new;
	size_t	i;

	i = 0;
	if ((new = (char *)malloc(sizeof(char) * (size + 1))))
	{
		while (i <= size)
		{
			new[i] = '\0';
			i++;
		}
	}
	else
		new = NULL;
	return (new);
}
